#include <stdio.h>

/**
 * main - entry point
 * Return: Always 0
 */

int main(void)
{
    int argc = 0;
    int i;

    for (i = 0; i == 0; argc++)
    {
        if (i < 1)
        {
            printf("%i\n", i);
            i++;
        }
        else
        {
            break;
        }
    }

    return 0;
}

